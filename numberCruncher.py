# number_cruncher.py
a = 10
b = 3
print(str(a) + " + " + str(b) + " = " + str(a + b))
print(str(a) + " - " + str(b) + " = " + str(a - b))
print(str(a) + " * " + str(b) + " = " + str(a * b))
print(str(a) + " / " + str(b) + " = " + str(a / b))
print(str(a) + " % " + str(b) + " = " + str(a % b))
print(str(a) + " ** " + str(b) + " = " + str(a ** b))
print("Is " + str(a) + " greater than " + str(b) + "? " + str(a > b))